var model = elise.model(1024, 768);
model.setFill('White');
var modelUrl = 'https://s3-us-west-2.amazonaws.com/schematrix.elise/Elise/Models/Christmas04.mdl';
var sketcher = elise.sketcher(modelUrl).addTo(model);
sketcher.timerDelay = 20;
sketcher.strokeBatchSize = 16;
sketcher.fillBatchSize = 32;
sketcher.sketchColor = false;
sketcher.strokeOpacity = 32;
return model;